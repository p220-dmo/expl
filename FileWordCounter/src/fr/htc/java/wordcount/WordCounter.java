package fr.htc.java.wordcount;

public class WordCounter {
	
	private int count;
	private String word;
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	
	

}
